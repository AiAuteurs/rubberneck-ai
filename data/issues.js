// Issues go live at 6am EST every morning
function getAdjustedDateStr() {
  const now = new Date();
  const utcMs = now.getTime() + now.getTimezoneOffset() * 60000;
  const estOffsetMs = -4 * 60 * 60000; // EST = UTC-5
  const estTime = new Date(utcMs + estOffsetMs);
  // Subtract 6 hours so 6am EST = start of "today"
  const adjustedTime = new Date(estTime.getTime() - 6 * 60 * 60000);
  return adjustedTime.toISOString().split("T")[0];
}

export function getTodaysIssue() {
  const todayStr = getAdjustedDateStr();
  const sortedDates = Object.keys(issues).sort((a, b) => (a > b ? -1 : 1));

  if (issues[todayStr]) {
    return { issue: issues[todayStr], isLatest: true, daysOld: 0, date: todayStr };
  }

  for (const date of sortedDates) {
    if (date <= todayStr) {
      const daysOld = Math.floor(
        (new Date(todayStr) - new Date(date)) / (1000 * 60 * 60 * 24)
      );
      return { issue: issues[date], isLatest: false, daysOld, date };
    }
  }

  const earliest = sortedDates[sortedDates.length - 1];
  return { issue: issues[earliest], isLatest: false, daysOld: null, date: earliest };
}

export function getAllPastIssues() {
  const todayStr = getAdjustedDateStr();
  return Object.keys(issues)
    .filter((date) => date <= todayStr)
    .sort((a, b) => (a > b ? -1 : 1))
    .map((date) => issues[date]);
}

export function getIssueBySlug(slug) {
  return Object.values(issues).find((i) => i.slug === slug) || null;
}

export function getIssueById(id) {
  return Object.values(issues).find((i) => i.id === Number(id)) || null;
}
